package com.gmail.furkanaxx34.dlibrary.input;

/**
 * an enum class that contains en reason for chat input.
 */
public enum EndReason {
  /**
   * the player cancels.
   */
  PLAYER_CANCELS,
  /**
   * the finish.
   */
  FINISH,
  /**
   * the expire.
   */
  EXPIRE,
  /**
   * the player disconnects.
   */
  PLAYER_DISCONNECTS,
  /**
   * the invalid input.
   */
  INVALID_INPUT,
  /**
   * the custom.
   */
  CUSTOM
}
